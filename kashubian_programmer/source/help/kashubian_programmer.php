<p>
  Kashubian Programmer follows the standard Programmer layout. Use AltGr (Right Alt) for Kashubian letters and Shift+AltGr for their uppercase forms (Caps Lock+AltGr works the same).
</p>

<ul>
  <li>AltGr+Q = ã, AltGr+Shift+Q = Ã</li>
  <li>AltGr+W = é, AltGr+Shift+W = É</li>
  <li>AltGr+R = ë, AltGr+Shift+R = Ë</li>
  <li>AltGr+P = ô, AltGr+Shift+P = Ô</li>
  <li>AltGr+0 = ò, AltGr+Shift+0 = Ò</li>
  <li>AltGr+U = ù, AltGr+Shift+U = Ù</li>
  <li>AltGr+A/C/E/L/N/O/S/X/Z for Polish ą, ć, ę, ł, ń, ó, ś, ź, ż (AltGr+Shift+X/Z for Ź/Ż)</li>
</ul>

